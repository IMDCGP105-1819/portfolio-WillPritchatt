# > is the greater than operator, it will return true if the item on the left is greater than the item on the right
print("Is it greater?", 5 > -2)
# >= is the greater than or equal to operator, it will return true if the item on the left is greater than or equal to
# the item on the right
print("Is it greater or equal?", 5 >= -2)
# <= is the less than or equal to operator, it will return true if the item on the left is less than or equal to the
# item on the right
print("Is it less or equal?", 5 <= -2)
